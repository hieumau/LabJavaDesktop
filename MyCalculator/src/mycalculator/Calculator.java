    private void loadScreen(){
        mainScreen.setText(mainText);
        if (mainText.equals("error")){
            mainText = "0";
            secondText = "";
            buffer = "0";
            operator = "";
            memory = "";
            secondScreen.setText("");
            return;
        }
        if (isMemoryEmpty() && buffer.equals("0")){
            secondScreen.setText("");
        } else {
            secondText = memory + " " + operator + " " + buffer;
            secondScreen.setText(secondText);
        }
    }
